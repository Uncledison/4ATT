export function dynamicOf(a: Animal, b: Animal): Dynamic {
  const [x, y] = [a, b].sort((p, q) => ANIMAL_ORDER.indexOf(p) - ANIMAL_ORDER.indexOf(q))
  return DYNAMICS[`${x}-${y}`]
}
